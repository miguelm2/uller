                        condensador, éste le roba calor al gas. Este proceso es conocido como sub enfriamiento.
                    </p>
                    <p>
                        En cuanto comienza a tomar calor, una parte del gas se convierte a gaseoso y otra se queda en estado 
                        líquido. Esta mezcla, viajará hasta la válvula de expansión. Esta válvula, lo que provocará es que el 
                        refrigerante pierda carga, lo que generará un descenso en la presión y la temperatura del gas. Para 
                        ser un poco más claros, podemos comparar este procedimiento con un spray, en el cual al oprimir, 
                        rociamos el líquido y éste sale frío.
                    </p>
